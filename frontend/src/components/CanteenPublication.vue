<template>
  <div class="text-left">
    <div v-if="diagnostic.valueTotalHt && (diagnostic.valueBioHt || diagnostic.valueSustainableHt)">
      <h2 class="font-weight-black text-h6 grey--text text--darken-4 my-4">
        Que mange-t-on dans les assiettes en {{ publicationYear }} ?
      </h2>
      <v-row>
        <v-col cols="12" sm="6" md="4" v-if="diagnostic.valueBioHt">
          <v-card class="fill-height text-center pa-4 d-flex flex-column justify-center" outlined>
            <p class="ma-0">
              <span class="grey--text text-h5 font-weight-black text--darken-2 mr-1">{{ bioPercent }} %</span>
              <span class="caption grey--text text--darken-2">
                bio
              </span>
            </p>
            <div class="mt-2">
              <v-img
                contain
                src="/static/images/quality-labels/logo_bio_eurofeuille.png"
                alt="Logo Agriculture Biologique"
                title="Logo Agriculture Biologique"
                max-height="35"
              />
            </div>
          </v-card>
        </v-col>
        <v-col cols="12" sm="6" md="4" v-if="diagnostic.valueSustainableHt">
          <v-card class="fill-height text-center pa-4 d-flex flex-column justify-center" outlined>
            <p class="ma-0">
              <span class="grey--text text-h5 font-weight-black text--darken-2 mr-1">{{ sustainablePercent }} %</span>
              <span class="caption grey--text text--darken-2">
                durables et de qualité
              </span>
            </p>
            <div class="d-flex mt-2 justify-center">
              <v-img
                contain
                v-for="label in labels"
                :key="label.title"
                :src="`/static/images/quality-labels/${label.src}`"
                :alt="label.title"
                :title="label.title"
                class="px-1"
                max-height="40"
                max-width="40"
              />
            </div>
          </v-card>
        </v-col>
      </v-row>
    </div>

    <h2 class="font-weight-black text-h6 grey--text text--darken-4 mt-8 mb-n4" v-if="Object.keys(earnedBadges).length">
      Nos démarches
    </h2>
    <v-row class="my-6">
      <v-col cols="12" v-for="(badge, key) in earnedBadges" :key="key">
        <v-card class="fill-height" elevation="0">
          <div class="d-flex align-start">
            <v-img width="30" max-width="35" contain :src="`/static/images/badge-${key}.svg`" alt=""></v-img>
            <div>
              <v-card-title class="py-0 text-body-2 font-weight-bold">{{ badge.title }}</v-card-title>
              <v-card-subtitle class="pt-4" v-text="badge.subtitle"></v-card-subtitle>
            </div>
          </div>
        </v-card>
      </v-col>
    </v-row>

    <div v-if="canteen && canteen.publicationComments">
      <h2 class="font-weight-black text-h6 grey--text text--darken-4 mb-2">
        Un petit mot du gestionnaire
      </h2>
      <p class="body-2">
        {{ canteen.publicationComments }}
      </p>
    </div>

    <div v-if="canteen && shouldDisplayGraph">
      <h2 class="font-weight-black text-h6 grey--text text--darken-4 mt-12 mb-2">
        L'évolution de l'approvisionnement
      </h2>
      <div>
        <MultiYearSummaryStatistics
          :diagnostics="graphDiagnostics"
          headingId="appro-heading"
          height="260"
          :width="$vuetify.breakpoint.mdAndUp ? '650px' : '100%'"
        />
      </div>
    </div>

    <div v-if="canteen && canteen.images && canteen.images.length > 0">
      <h2 class="font-weight-black text-h6 grey--text text--darken-4 mt-8 mb-0">
        Images
      </h2>
      <p class="body-2">
        Cliquez sur une image pour l'agrandir
      </p>
      <div>
        <ImageGallery :images="canteen.images" />
      </div>
    </div>
  </div>
</template>

<script>
import labels from "@/data/quality-labels.json"
import { lastYear, earnedBadges, getPercentage, isDiagnosticComplete } from "@/utils"
import MultiYearSummaryStatistics from "@/components/MultiYearSummaryStatistics"
import ImageGallery from "@/components/ImageGallery"

export default {
  props: {
    canteen: Object,
  },
  data() {
    return {
      labels,
      publicationYear: lastYear(),
    }
  },
  components: { MultiYearSummaryStatistics, ImageGallery },
  computed: {
    diagnostic() {
      return this.canteen.diagnostics.find((d) => d.year === this.publicationYear) || {}
    },
    bioPercent() {
      return getPercentage(this.diagnostic.valueBioHt, this.diagnostic.valueTotalHt)
    },
    sustainablePercent() {
      return getPercentage(this.diagnostic.valueSustainableHt, this.diagnostic.valueTotalHt)
    },
    earnedBadges() {
      return earnedBadges(this.canteen, this.diagnostic, this.$store.state.sectors)
    },
    shouldDisplayGraph() {
      if (!this.canteen.diagnostics || this.canteen.diagnostics.length === 0) return false
      const completedDiagnostics = this.canteen.diagnostics.filter(isDiagnosticComplete)
      if (completedDiagnostics.length === 0) return false
      else if (completedDiagnostics.length === 1) return completedDiagnostics[0].year !== lastYear()
      else return true
    },
    graphDiagnostics() {
      if (!this.canteen.diagnostics || this.canteen.diagnostics.length === 0) return null
      const diagnostics = {}
      for (let i = 0; i < this.canteen.diagnostics.length; i++) {
        const diagnostic = this.canteen.diagnostics[i]
        diagnostics[diagnostic.year] = diagnostic
      }
      return diagnostics
    },
  },
}
</script>
